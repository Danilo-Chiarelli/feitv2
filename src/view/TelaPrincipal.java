    private void CurtirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CurtirActionPerformed
int linha = jTable1.getSelectedRow();
        
        if (linha == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um vídeo na tabela para curtir!");
        } else {
            // Pega o ID da linha selecionada
            int id = Integer.parseInt(jTable1.getValueAt(linha, 0).toString());
            
            // Manda o DAO curtir
            new dao.VideoDAO().curtirVideo(id);
            
            // Atualiza a tabela automaticamente para você ver o número subir na hora
            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0); 
            for(Object[] l : new dao.VideoDAO().listarVideos()) {
                modelo.addRow(l);
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_CurtirActionPerformed

