    private void BTAddFavoritosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTAddFavoritosActionPerformed

        int linha = jTable1.getSelectedRow();
        
        if (linha == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um vídeo na tabela primeiro!");
        } else {
            // Pega o ID do vídeo que você clicou
            int idVideo = Integer.parseInt(jTable1.getValueAt(linha, 0).toString());
            
            // Abre uma caixinha pedindo o ID da playlist
            String resposta = javax.swing.JOptionPane.showInputDialog(this, "Digite o ID da sua Playlist (veja na tela de Gerenciar Favoritos):");
            
            if (resposta != null && !resposta.isEmpty()) {
                int idPlaylist = Integer.parseInt(resposta);
                
                // Manda o DAO conectar os dois!
                new dao.PlaylistDAO().adicionarVideo(idPlaylist, idVideo);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Vídeo adicionado à playlist com sucesso!");
            }
        }

    }//GEN-LAST:event_BTAddFavoritosActionPerformed

    private void btExcluirFavoritosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirFavoritosActionPerformed

        int linha = jTable1.getSelectedRow();
        
        if (linha == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um vídeo na tabela primeiro!");
        } else {
            int idVideo = Integer.parseInt(jTable1.getValueAt(linha, 0).toString());
            
            String resposta = javax.swing.JOptionPane.showInputDialog(this, "De qual Playlist você quer remover esse vídeo? (Digite o ID):");
            
            if (resposta != null && !resposta.isEmpty()) {
                int idPlaylist = Integer.parseInt(resposta);
                
                new dao.PlaylistDAO().removerVideo(idPlaylist, idVideo);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Vídeo removido da playlist!");
            }
        }

    }//GEN-LAST:event_btExcluirFavoritosActionPerformed

