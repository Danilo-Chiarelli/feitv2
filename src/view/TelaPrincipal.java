    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed

        int linhaSelecionada = jTable1.getSelectedRow();
        
        if (linhaSelecionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, clique em um vídeo na tabela antes de excluir!");
        } else {
            
            String idTexto = jTable1.getValueAt(linhaSelecionada, 0).toString();
            int idParaApagar = Integer.parseInt(idTexto);
            
            dao.VideoDAO dao = new dao.VideoDAO();
            dao.excluirVideo(idParaApagar);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Vídeo apagado com sucesso! Clique em 'atualiza' para ver a tabela limpa.");
        }

    }//GEN-LAST:event_ExcluirActionPerformed

    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed

        int linha = jTable1.getSelectedRow();
        if (linha == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um vídeo para editar!");
        } else {
            String id = jTable1.getValueAt(linha, 0).toString();
            String titulo = jTable1.getValueAt(linha, 1).toString();
            String tipo = jTable1.getValueAt(linha, 2).toString();
            String genero = jTable1.getValueAt(linha, 3).toString();
            String situacao = jTable1.getValueAt(linha, 4).toString();

            TelaCadastro tela = new TelaCadastro();
            tela.preencherCampos(id, titulo, tipo, genero, situacao); 
            tela.setVisible(true);
            tela.setLocationRelativeTo(null);
        
        
}    }//GEN-LAST:event_EditarActionPerformed

